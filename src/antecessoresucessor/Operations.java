package antecessoresucessor;

public class Operations {

    public static void predecessorAndSuccessor(int number) {
        System.out.println("Number: " + number);
        System.out.println("Predecessor of " + number + ": " + (number - 1));
        System.out.println("Successor of " + number + ": " + (number + 1));
    }

}
